import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { buildProtoFileRules, transformGeneratedContent } from "./proto-transform.js";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = path.resolve(SCRIPT_DIR, "..");

const SRC_API_DIR = path.resolve(ROOT_DIR, "src/api");
const PROTO_DIR = path.resolve(ROOT_DIR, "src/api/proto");
const OUT_DIR = path.resolve(ROOT_DIR, "src/api/messages");
const TYPES_INDEX_PATH = path.resolve(ROOT_DIR, "src/api/types.ts");
const CONSTANTS_INDEX_PATH = path.resolve(ROOT_DIR, "src/api/constants.ts");

const CUSTOM_GENERATED_HEADER_LINES = ["", "// @ts-nocheck", "// oxlint-disable", ""];

(async function main() {
  const protoFiles = await runStep(`Collecting proto files from ${PROTO_DIR}`, () =>
    collectProtoFiles(PROTO_DIR),
  );
  await runStep(`Removing previous generated files in ${OUT_DIR}`, () =>
    resetGeneratedDirectory(OUT_DIR),
  );
  await runStep("Generating new proto files", () => generateProtoFiles(protoFiles));
  await runStep("Applying project-level proto file transformations", () =>
    applyProtoTransforms(protoFiles),
  );
  await runStep("Inserting autogenerated file headers comments", () =>
    insertAutogeneratedHeaders(),
  );
  await runStep(`Generating index file ${TYPES_INDEX_PATH} for types`, () => generateTypesIndex());
  await runStep(`Generating index file ${CONSTANTS_INDEX_PATH} for the constants`, () =>
    generateConstantsIndex(),
  );
  await runStep("Applying formatting", () => formatGeneratedFiles());

  console.log("✓ Proto generation complete!");
})();

async function runStep(name, fn) {
  try {
    console.log(`${name}…`);
    const result = await fn();
    console.log(`✓`);
    return result;
  } catch (error) {
    console.error(`✗`, error);
    process.exit(1);
  }
}

function collectProtoFiles(protoDir) {
  const protoFiles = listFilesRecursively(protoDir, /\.proto$/);

  if (protoFiles.length === 0) {
    throw new Error(`No "*.proto" files found in ${protoDir}`);
  } else {
    console.log(`Found ${protoFiles.length} proto files:`, protoFiles);
  }

  return protoFiles;
}

function resetGeneratedDirectory(outputDir) {
  fs.rmSync(outputDir, { recursive: true, force: true });
  fs.mkdirSync(outputDir, { recursive: true });
}

function generateProtoFiles(protoFiles) {
  const pluginPath = path.resolve(ROOT_DIR, "node_modules/.bin/protoc-gen-ts_proto");

  const command = [
    "protoc",
    `--plugin=${pluginPath}`,
    `--ts_proto_out=${OUT_DIR}`,
    `--ts_proto_opt=${[
      "env=both",
      "oneof=unions-value",
      "exportCommonSymbols=false",
      "esModuleInterop=true",
      "outputEncodeMethods=false",
      "snakeToCamel=keys",
      "protoJsonFormat=false",
      "useNumericEnumForJson=true",
      "stringEnums=true",
      "emitDefaultValues=json-methods",
      "outputServices=false",
      "enumsAsLiterals=true",
    ].join(",")}`,
    `--proto_path=${PROTO_DIR}`,
    ...protoFiles,
  ].join(" ");

  execSync(command, { stdio: "inherit" });
}

function formatGeneratedFiles() {
  const targets = [OUT_DIR, TYPES_INDEX_PATH, CONSTANTS_INDEX_PATH].filter((target) =>
    fs.existsSync(target),
  );

  if (targets.length === 0) return;

  const targetArgs = targets.map((target) => `"${target.replaceAll('"', '\\"')}"`).join(" ");

  execSync(`pnpm oxfmt --write ${targetArgs}`, { stdio: "inherit" });
}

async function insertAutogeneratedHeaders() {
  const generatedFiles = listFilesRecursively(OUT_DIR, /\.ts$/);

  for (const filePath of generatedFiles) {
    const content = await fs.promises.readFile(filePath, "utf-8");
    const lines = content.split("\n");

    const nextContent = [
      ...lines.slice(0, 5),
      ...CUSTOM_GENERATED_HEADER_LINES,
      ...lines.slice(7),
    ].join("\n");

    await fs.promises.writeFile(filePath, nextContent, "utf-8");
  }
}

async function applyProtoTransforms(protoFiles) {
  for (const protoFilePath of protoFiles) {
    const relativeProtoPath = path.relative(PROTO_DIR, protoFilePath);
    const generatedTsPath = path.resolve(OUT_DIR, relativeProtoPath.replace(/\.proto$/, ".ts"));

    if (!fs.existsSync(generatedTsPath)) continue;

    const rules = buildProtoFileRules({ protoDir: PROTO_DIR, protoFilePath });

    if (rules.size === 0) continue;

    const content = await fs.promises.readFile(generatedTsPath, "utf-8");
    const nextContent = transformGeneratedContent(content, rules);
    await fs.promises.writeFile(generatedTsPath, nextContent, "utf-8");
  }
}

async function generateTypesIndex() {
  const TYPE_EXPORT_RE = /^export (?:interface|type) (\w+)/gm;
  const EXCLUDED_TYPE_NAMES = new Set(["Empty"]);

  const generatedFiles = listFilesRecursively(OUT_DIR, /\.ts$/);
  const lines = [];

  for (const filePath of generatedFiles) {
    const content = await fs.promises.readFile(filePath, "utf-8");
    const exportedNames = [...content.matchAll(TYPE_EXPORT_RE)]
      .map((match) => match[1])
      .filter((name) => !EXCLUDED_TYPE_NAMES.has(name));

    if (exportedNames.length === 0) continue;

    lines.push(
      `export type { ${exportedNames.join(", ")} } from "${toApiModuleImportPath(filePath)}";`,
    );
  }

  await writeIndexFile(TYPES_INDEX_PATH, lines);
}

async function generateConstantsIndex() {
  const CONST_EXPORT_RE = /^export const (\w+) = \{[^}]*\} as const;/gms;
  const EXCLUDED_CONST_NAMES = new Set();

  const generatedFiles = listFilesRecursively(OUT_DIR, /\.ts$/);
  const lines = [];

  for (const filePath of generatedFiles) {
    const content = await fs.promises.readFile(filePath, "utf-8");
    const exportedNames = [...content.matchAll(CONST_EXPORT_RE)]
      .map((match) => match[1])
      .filter((name) => !EXCLUDED_CONST_NAMES.has(name));

    if (exportedNames.length === 0) continue;

    lines.push(`export { ${exportedNames.join(", ")} } from "${toApiModuleImportPath(filePath)}";`);
  }

  await writeIndexFile(CONSTANTS_INDEX_PATH, lines);
}

async function writeIndexFile(outputPath, lines) {
  const content = [
    "// Code generated by scripts/generate-proto.js. DO NOT EDIT.",
    ...CUSTOM_GENERATED_HEADER_LINES,
    ...lines,
  ].join("\n");

  await fs.promises.writeFile(outputPath, content, "utf-8");
}

function toApiModuleImportPath(filePath) {
  return `./${path.relative(SRC_API_DIR, filePath).replace(/\\/g, "/").replace(/\.ts$/, "")}`;
}

function listFilesRecursively(rootDir, pattern, found = []) {
  for (const entryName of fs.readdirSync(rootDir)) {
    const fullPath = path.join(rootDir, entryName);
    const stat = fs.statSync(fullPath);

    if (stat.isDirectory()) {
      listFilesRecursively(fullPath, pattern, found);
      continue;
    }

    if (pattern.test(entryName)) {
      found.push(fullPath);
    }
  }

  return found;
}
